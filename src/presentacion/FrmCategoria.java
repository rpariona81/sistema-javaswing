    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtNombre.getText().trim().length()==0) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un nombre, es obligatorio","Sistema",JOptionPane.WARNING_MESSAGE);
            txtNombre.requestFocus();
            return;
        }
        String resp;
        if (this.accion.equals("editar")) {
            
        }else{
            resp=this.CONTROL.insertar(txtNombre.getText().trim(), txtDescripcion.getText().trim());
            if (resp.equals("OK")) {
                this.mensajeOk("Regisrado correctamente");
                this.limpiar();
                this.listar("");
            }else{
                this.mensajeError(resp);
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


